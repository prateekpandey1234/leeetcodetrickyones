/**Good question
 * solution
 * :so basically we have to observe the pattern hwo the matrix is getting rotated ... it's clockwise,
 * or we can do its transpose and then reverse the matrix along the rows*/
public class RotateMatrix {
    public void rotate(int[][] matrix) {
        transpose(matrix);
        Reverse(matrix);
    }

    public void transpose(int[][] matrix) {
        int n = matrix.length;
        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                int tmp = matrix[j][i];
                matrix[j][i] = matrix[i][j];
                matrix[i][j] = tmp;
            }
        }
    }

    public void Reverse(int[][] matrix) {
        int n = matrix.length;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n / 2; j++) {
                int tmp = matrix[i][j];
                matrix[i][j] = matrix[i][n - j - 1];
                matrix[i][n - j - 1] = tmp;
            }
        }
    }
}
